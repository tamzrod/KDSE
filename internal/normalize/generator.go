func (g *Generator) generateGlossary(result *NormalizationResult) (*NormalizedArtifact, error) {
	var buf strings.Builder
	buf.WriteString("# Glossary\n\n")
	buf.WriteString("**Document Version:** " + kdseVersion + "\n")
	buf.WriteString("**Type:** Engineering Artifact\n")
	buf.WriteString("**Phase:** Derived\n\n")
	buf.WriteString("---\n\n")
	buf.WriteString("## Terms\n\n")

	for i, term := range result.KnowledgeExtract.Glossary {
		buf.WriteString(fmt.Sprintf("### %s\n\n", term.Term))
		buf.WriteString(fmt.Sprintf("%s\n\n", term.Definition))
		buf.WriteString(fmt.Sprintf("*Source: %s*\n\n", term.Source))
		if i < len(result.KnowledgeExtract.Glossary)-1 {
			buf.WriteString("---\n\n")
		}
	}

	buf.WriteString("---\n\n")
	buf.WriteString(g.formatTraceabilityBlock(result, "GLOSSARY.md"))
	buf.WriteString("\n\n*This glossary was generated by KDSE Normalization from existing documentation.*\n")
	buf.WriteString("*Original documentation is preserved in its original location.*\n")
	buf.WriteString(fmt.Sprintf("*Generated: %s*\n", time.Now().Format(time.RFC3339)))

	content := buf.String()
	path := filepath.Join(g.kdseDir, "GLOSSARY.md")
	if err := g.writeArtifact(path, content); err != nil {
		return nil, err
	}

	return &NormalizedArtifact{
		ID:       "NORM-GLOSS-" + g.sessionID,
		Path:     path,
		DocType:  "Glossary",
		Title:    "Project Glossary",
		Content:  content,
		Traceability: Traceability{
			DerivedFrom:       g.getDerivedFromFromGlossary(result.KnowledgeExtract.Glossary),
			OriginalDocuments: g.getOriginalDocPaths(result),
			Authority:         g.getAuthority(result),
			Version:           kdseVersion,
			NormativeRefs:     []string{"docs/foundation/007-glossary.md"},
			TransformationType: "Normalization",
		},
		Metadata: ArtifactMetadata{
			Version:     kdseVersion,
			KDSEVersion: kdseVersion,
			Phase:       string(types.PhaseDefined),
			Tags:        []string{"normalized", "glossary"},
		},
		CreatedAt: time.Now().Format(time.RFC3339),
	}, nil
}

