func (g *Generator) generateArtifactsIndex(result *NormalizationResult, artifacts []NormalizedArtifact) (*NormalizedArtifact, error) {
	var buf strings.Builder
	buf.WriteString("# KDSE Artifacts Index\n\n")
	buf.WriteString("**Document Version:** " + kdseVersion + "\n")
	buf.WriteString("**Type:** Engineering Artifact\n")
	buf.WriteString("**Phase:** Derived\n\n")
	buf.WriteString("---\n\n")
	buf.WriteString("## Overview\n\n")
	buf.WriteString("This index lists all KDSE-standard artifacts generated from existing documentation.\n\n")
	buf.WriteString(fmt.Sprintf("**Session ID:** %s\n", result.SessionID))
	buf.WriteString(fmt.Sprintf("**Normalized:** %s\n\n", result.CompletedAt))

	buf.WriteString("## Artifacts\n\n")
	buf.WriteString("| Artifact | Path | Traceability |\n")
	buf.WriteString("|----------|------|-------------|\n")
	for _, artifact := range artifacts {
		buf.WriteString(fmt.Sprintf("| %s | %s | [Details](#%s) |\n",
			artifact.Title,
			filepath.Base(artifact.Path),
			strings.ToLower(strings.ReplaceAll(artifact.Title, " ", "-"))))
	}

	buf.WriteString("\n## Traceability Matrix\n\n")
	buf.WriteString("| KDSE Artifact | Derived From | Original Documents |\n")
	buf.WriteString("|---------------|--------------|-------------------|\n")
	for _, artifact := range artifacts {
		derivedFrom := ""
		for i, df := range artifact.Traceability.DerivedFrom {
			if i > 0 {
				derivedFrom += ", "
			}
			derivedFrom += filepath.Base(df.Path)
		}
		origDocs := ""
		for i, doc := range artifact.Traceability.OriginalDocuments {
			if i > 0 {
				origDocs += ", "
			}
			origDocs += filepath.Base(doc)
		}
		buf.WriteString(fmt.Sprintf("| %s | %s | %s |\n",
			artifact.Title,
			derivedFrom,
			origDocs))
	}

	buf.WriteString("\n## Preserved Documentation\n\n")
	buf.WriteString("The following original documentation was preserved unchanged:\n\n")
	for _, doc := range result.PreservedDocs {
		buf.WriteString(fmt.Sprintf("- %s\n", doc))
	}

	buf.WriteString("\n---\n\n")
	buf.WriteString(g.formatTraceabilityBlock(result, "ARTIFACTS.md"))
	buf.WriteString("\n\n*This index was generated by KDSE Normalization.*\n")
	buf.WriteString("*Original documentation is preserved in its original location.*\n")
	buf.WriteString(fmt.Sprintf("*Generated: %s*\n", time.Now().Format(time.RFC3339)))

	content := buf.String()
	path := filepath.Join(g.kdseDir, "ARTIFACTS.md")
	if err := g.writeArtifact(path, content); err != nil {
		return nil, err
	}

	return &NormalizedArtifact{
		ID:       "NORM-IDX-" + g.sessionID,
		Path:     path,
		DocType:  "Artifacts Index",
		Title:    "KDSE Artifacts Index",
		Content:  content,
		Traceability: Traceability{
			DerivedFrom:       []DerivedFrom{},
			OriginalDocuments: g.getOriginalDocPaths(result),
			Authority:         g.getAuthority(result),
			Version:           kdseVersion,
			NormativeRefs:     []string{"docs/foundation/005-engineering-artifacts.md"},
			TransformationType: "Index",
		},
		Metadata: ArtifactMetadata{
			Version:     kdseVersion,
			KDSEVersion: kdseVersion,
			Phase:       string(types.PhaseValidated),
			Tags:        []string{"normalized", "index"},
		},
		CreatedAt: time.Now().Format(time.RFC3339),
	}, nil
}

// Helper methods

func (g *Generator) writeArtifact(path, content string) error {
	return os.WriteFile(path, []byte(content), 0644)
}

func (g *Generator) filterByType(docs []DiscoveredDocument, types ...DocType) []DiscoveredDocument {
	typeMap := make(map[DocType]bool)
	for _, t := range types {
		typeMap[t] = true
	}

	var filtered []DiscoveredDocument
	for _, doc := range docs {
		if typeMap[doc.Type] {
			filtered = append(filtered, doc)
		}
	}
	return filtered
}

func (g *Generator) getAuthority(result *NormalizationResult) string {
	if len(result.KnowledgeExtract.Stakeholders) > 0 {
		return result.KnowledgeExtract.Stakeholders[0]
	}
	return "Project Maintainers"
}

func (g *Generator) getOriginalDocPaths(result *NormalizationResult) []string {
	var paths []string
	for _, doc := range result.DiscoveredDocs {
		paths = append(paths, doc.Path)
	}
	return paths
}

func (g *Generator) getDerivedFrom(result *NormalizationResult, types ...DocType) []DerivedFrom {
	typeMap := make(map[DocType]bool)
	for _, t := range types {
		typeMap[t] = true
	}

	var derived []DerivedFrom
	for _, doc := range result.DiscoveredDocs {
		if typeMap[doc.Type] {
			derived = append(derived, DerivedFrom{
				Path:     doc.Path,
				DocType:  string(doc.Type),
				Confidence: fmt.Sprintf("%.0f%%", doc.Confidence*100),
			})
		}
	}
	return derived
}

func (g *Generator) getDerivedFromFromGlossary(glossary []GlossaryTerm) []DerivedFrom {
	sourceMap := make(map[string]bool)
	var derived []DerivedFrom
	for _, term := range glossary {
		if !sourceMap[term.Source] {
			sourceMap[term.Source] = true
			derived = append(derived, DerivedFrom{
				Path:     term.Source,
				DocType:  "Glossary",
				Confidence: "100%",
			})
		}
	}
	return derived
}

func (g *Generator) formatPurpose(k KnowledgeExtraction) string {
	if k.Purpose != "" {
		return k.Purpose
	}
	return "This project provides [see original documentation for details]"
}

func (g *Generator) formatDomain(k KnowledgeExtraction) string {
	if k.Domain != "" {
		return k.Domain
	}
	return "Software Engineering"
}

func (g *Generator) formatStakeholders(k KnowledgeExtraction) string {
	if len(k.Stakeholders) == 0 {
		return "See original documentation for stakeholder information"
	}
	var buf strings.Builder
	for _, s := range k.Stakeholders {
		buf.WriteString(fmt.Sprintf("- %s\n", s))
	}
	return buf.String()
}

func (g *Generator) formatRequirements(k KnowledgeExtraction) string {
	if len(k.Requirements) == 0 {
		return "See original documentation for requirements"
	}
	var buf strings.Builder
	for i, r := range k.Requirements {
		buf.WriteString(fmt.Sprintf("%d. %s\n", i+1, r.Description))
		buf.WriteString(fmt.Sprintf("   - Source: %s\n", r.Source))
	}
	return buf.String()
}

func (g *Generator) formatConstraints(k KnowledgeExtraction) string {
	if len(k.Constraints) == 0 {
		return "No explicit constraints defined"
	}
	var buf strings.Builder
	for _, c := range k.Constraints {
		buf.WriteString(fmt.Sprintf("- %s\n", c))
	}
	return buf.String()
}

func (g *Generator) formatOverview(result *NormalizationResult) string {
	return fmt.Sprintf("This architecture was derived from %d existing documentation files.",
		len(result.DiscoveredDocs))
}

func (g *Generator) formatDecisions(k KnowledgeExtraction) string {
	if len(k.Decisions) == 0 {
		return "See original documentation for architectural decisions"
	}
	var buf strings.Builder
	for _, d := range k.Decisions {
		buf.WriteString(fmt.Sprintf("- [%s] %s: %s\n", d.ID, d.Title, d.Decision))
	}
	return buf.String()
}

