func (g *Generator) formatTraceabilityBlock(result *NormalizationResult, artifactName string) string {
	var buf strings.Builder
	buf.WriteString("## Traceability\n\n")
	buf.WriteString(fmt.Sprintf("**Artifact ID:** NORM-%s\n", result.SessionID))
	buf.WriteString(fmt.Sprintf("**Derived From:** %d source documents\n", len(result.DiscoveredDocs)))
	buf.WriteString("**Original Documents:**\n")
	for _, doc := range result.DiscoveredDocs {
		buf.WriteString(fmt.Sprintf("  - %s (%s)\n", doc.Path, doc.Type))
	}
	return buf.String()
}
